    private void jTextField1FocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextField1FocusLost
        if(!nombreAntiguo.equals(jTextField1.getText())){
            int i = JOptionPane.showConfirmDialog(frame, "Seguro que quieres cambiar costo modificacion: "+nombreAntiguo+
                "\npor "+jTextField1.getText());
            if(i==0){
                for (int j = 0; j < EntityDB.getInstance().getPlanesDeSoporte().size(); j++) {
                    if(((PlanDeSoporte)EntityDB.getInstance().getPlanesDeSoporte().get(j)).getNombre().equals(nombreAntiguo)){
                        ((PlanDeSoporte)EntityDB.getInstance().getPlanesDeSoporte().get(j)).setNombre(jTextField1.getText());
                    }
                }
            }
            nombreAntiguo = jTextField1.getText();
        }
    }//GEN-LAST:event_jTextField1FocusLost

